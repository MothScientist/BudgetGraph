    @staticmethod
    def check_lang_in_dict(language: str) -> bool:
        if language in Dictionary._languages.keys():
            return True
        return False

    @staticmethod
    def check_phrase_in_dict(language: str, phrase: str) -> bool:
        if phrase in Dictionary._languages[language]:
            return True
        return False

    @staticmethod
    def receive_translation(language: str, phrase: str) -> str:
        """
        The function takes as input a phrase that the chatbot responds to the user in the selected language
        (also passed in the parameters).
        These phrases are known in advance and are found along with translations in the languages dictionary.

        Args:
            language (str): one of the valid language codes -> en / ru / es / fr / de / is
            phrase (str): string that is a key in the language dictionary.

        Returns:
            str: value in the dictionary in the selected language
        """
        return Dictionary._languages[language].get(phrase)
